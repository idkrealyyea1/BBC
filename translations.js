var currentLang = localStorage.getItem('bbcLang') || 'en';
var currentTheme = localStorage.getItem('bbcTheme') || 'dark';

function t(key) {
  return LANG[currentLang][key] || key;
}

function applyLanguage() {
  document.documentElement.dir = currentLang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = currentLang;
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    var translation = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = translation;
    } else if (el.tagName === 'A' && el.hasAttribute('href')) {
      if (el.querySelector('span')) {
        var span = el.querySelector('span');
        if (!span.getAttribute('data-i18n')) span.textContent = translation;
      } else {
        el.textContent = translation;
      }
    } else {
      el.innerHTML = translation;
    }
  });
  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === currentLang);
  });
}

function applyTheme() {
  document.documentElement.classList.toggle('light', currentTheme === 'light');
  document.querySelectorAll('.theme-btn').forEach(function(btn) {
    btn.classList.toggle('active', btn.getAttribute('data-theme') === currentTheme);
  });
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('bbcLang', lang);
  applyLanguage();
}

function setTheme(theme) {
  currentTheme = theme;
  localStorage.setItem('bbcTheme', theme);
  applyTheme();
}
